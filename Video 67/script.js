// ---
// Common DOM navigation properties:
// element.parentNode                // The parent node
// element.childNodes                // All child nodes (including text/comments)
// element.children                  // Only child element nodes
// element.firstChild                // The first child node (could be text/comment/element)
// element.lastChild                 // The last child node
// element.firstElementChild         // The first child that is an element
// element.lastElementChild          // The last child that is an element
// element.nextSibling               // The next sibling node (could be text/comment/element)
// element.previousSibling           // The previous sibling node
// element.nextElementSibling        // The next sibling that is an element
// element.previousElementSibling    // The previous sibling that is an element